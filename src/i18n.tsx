const faCaseInsensitive = new Map(Object.entries(fa).map(([key, value]) => [key.toLowerCase(), value]));

type I18nContextValue = {
  locale: Locale;
  dir: Direction;
  setLocale: (locale: Locale) => void;
  t: (key: string, vars?: Record<string, string | number>) => string;
  tv: (value?: string | number) => string;
  formatNumber: (value: number) => string;
  isRtl: boolean;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ locale, setLocale, children }: { locale: Locale; setLocale: (locale: Locale) => void; children: ReactNode }) {
  const value: I18nContextValue = {
    locale,
    dir: getDirection(locale),
    setLocale,
    t: (key, vars) => interpolate(translate(locale, key), vars),
    tv: (valueToTranslate) => translateValue(locale, valueToTranslate),
    formatNumber: (valueToFormat) => formatNumber(locale, valueToFormat),
    isRtl: locale === "fa",
  };

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) throw new Error("useI18n must be used inside I18nProvider");
  return context;
}

export function isLocale(value: string | undefined): value is Locale {
  return value === "en" || value === "fa";
}

export function getDirection(locale: Locale): Direction {
  return locale === "fa" ? "rtl" : "ltr";
}

export function getStoredLocale(): Locale | null {
  const value = window.localStorage.getItem(LOCALE_STORAGE_KEY);
  return isLocale(value ?? undefined) ? value as Locale : null;
}

export function storeLocale(locale: Locale) {
  window.localStorage.setItem(LOCALE_STORAGE_KEY, locale);
}

export function translate(locale: Locale, key: string) {
  const source = locale === "fa" ? fa : en;
  if (source[key]) return source[key];
  if (locale === "fa") return faCaseInsensitive.get(key.toLowerCase()) ?? key;
  return key;
}

export function translateValue(locale: Locale, value?: string | number) {
  if (value === undefined || value === null) return "";
  const raw = String(value);
  const direct = translate(locale, raw);
  if (direct !== raw) return direct;
  for (const [pattern, replacer] of patternTranslations) {
    const match = raw.match(pattern);
    if (match) return replacer(match, locale);
  }
  if (locale === "fa") return translateDomainFallback(raw);
  return raw;
}

function translateDomainFallback(raw: string) {
  const normalized = raw.replaceAll("_", " ").replaceAll("-", " ").replace(/\s+/g, " ").trim();
  if (!normalized || normalized === raw) return raw;
  const direct = translate("fa", normalized);
  if (direct !== normalized) return direct;
  const translatedTokens = normalized.split(" ").map((token) => translate("fa", token));
  return translatedTokens.join(" ");
}

export function formatNumber(locale: Locale, value: number) {
  return new Intl.NumberFormat(locale === "fa" ? "fa-IR" : "en-US", { maximumFractionDigits: 0 }).format(value);
}

function interpolate(value: string, vars?: Record<string, string | number>) {
  if (!vars) return value;
  return Object.entries(vars).reduce((text, [key, replacement]) => text.replaceAll(`{${key}}`, String(replacement)), value);
}

function isolate(value: string) {
  return `\u2068${value}\u2069`;
}
